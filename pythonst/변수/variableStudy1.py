# 변수

age = 30
name = '홍길동'
print("안녕하세요. 저는 서울사는 {}살 {}입니다.".format(age, name))
# 포맷에서 괄호안에 있는 age와 name은 값이 됩니다.
# 그 이유는 30을 대입시켰고 홍길동을 대입시켰기 때문에 값으로 나타나집니다.
# 순서도 중요합니다 name을 먼저 넣게 된다면 홍길동이라는 이름이 먼저 출력이 됩니다.

