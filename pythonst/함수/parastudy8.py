# plus 함수 구현

def plus(n1, n2):
  return n1 + n2
print("3 더하기 5는 {} 입니다.".format(plus(3, 5)))

def plus(n1, n2, n3):
  return n1 + n2 + n3
print("3 더하기 5 더하기 7은 {} 입니다.".format(plus(3, 5, 7)))

def minus(n1, n2):
  return n1 - n2
print("10 빼기 5는 {} 입니다.".format(minus(10, 5)))